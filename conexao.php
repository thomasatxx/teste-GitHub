<?php

class Banco
{
    private static $dbNome = 'teste'; // NOME DO BANCO
    private static $dbHost = 'localhost'; // NOME DA CONEXAO
    private static $dbUsuario = 'root';   // NOME DO USUARIO DO BANCO 

    
    private static $cont = null;
    
    public function __construct() 
    {
        die('A função Init nao é permitido!');
    }
    
    public static function conectar()
    {
        if(null == self::$cont)
        {
            try
            {
                self::$cont =  new PDO( "mysql:host=".self::$dbHost.";"."dbname=".self::$dbNome, self::$dbUsuario); 
            }
            catch(PDOException $exception)
            {
                die($exception->getMessage());
            }
        }
        return self::$cont;
    }
    
    public static function desconectar()
    {
        self::$cont = null;
    }
}

?>